#include "libft.h"

char	*ft_strjoin(char const *s1, char const *s2)
{
	int		i;
	char	*str;
	size_t	s1_len;
	size_t	s2_len;

	if (!s1 || !s2)
		return (NULL);
	s1_len = ft_strlen(s1);
	s2_len = ft_strlen(s2);
	if (!(str = ft_strnew(s1_len + s2_len)))
		return (NULL);
	i = 0;
	str = ft_strcpy(str, s1);
	ft_strcat(str, s2);
	return (str);
}
